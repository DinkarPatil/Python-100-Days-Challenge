# Attributes

"""
- Attributes are variables that belong to an object.
- They define the characteristics or properties of the object.
- These are accessed using dot notation (object.attribute).
- Attributes can be either instance attributes (unique to each instance of a class) or 
class attributes (shared among all instances of the class).
"""


# Methods

"""
- Methods are functions that are defined inside a class and operate on objects created from that class.
- They perform operations that either modify the state of the object or provide functionality related to the object.
- Methods are called using dot notation (object.method()).
- Like functions, methods can accept parameters and return values.
"""


# Class:

"""
- A class is a blueprint for creating objects (instances).
- It serves as a template that defines the attributes (variables) and methods (functions) 
that will be associated with the objects created from it.
- Classes are defined using the class keyword followed by the class name and a colon.
- Inside a class, you define methods (functions) that operate on instances of the class, 
as well as attributes (variables) that store data related to each instance.
"""


# Object (Instance):

"""
- An object (or instance) is a concrete entity created from a class.
- It represents a specific instance of the class, with its own unique data and behavior.
- Objects are created using the class name followed by parentheses (ClassName()).
- Each object has its own set of attributes and methods defined by its class.
"""


